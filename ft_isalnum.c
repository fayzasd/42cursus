#include "libft.h"

int	ft_isalnum(int val)
{
	if (('a' <= val && val <= 'z') || ('A' <= val && val <= 'Z'))
		return (1);
	else if ('0' <= val && val <= '9')
		return (1);
	else
		return (0);
}
